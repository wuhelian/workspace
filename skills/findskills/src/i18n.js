  // 翻译查询
  static translateQuery(query) {
    const lang = this.detectLanguage(query);
    if (lang === 'zh') {
      // 中文 -> 英文
      let translated = query;
      for (const [zh, en] of Object.entries(this.translationMap)) {
        if (translated.includes(zh)) {
          translated = translated.replace(new RegExp(zh, 'g'), en);
        }
      }
      return translated;
    } else {
      // 英文 -> 中文（可选）
      return query;
    }
  }

  // 扩展翻译（如果需要）
  static extendTranslationMap(newTranslations) {
    this.translationMap = { ...this.translationMap, ...newTranslations };
  }
